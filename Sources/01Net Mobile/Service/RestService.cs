					//-----------------------------------------------------------------------------------------------------------
					case RestRequestResult.NotFound           : Content = 
						 string.Format ( Content, "NOT_FOUND"           , "block", "none" ); break;
					//-----------------------------------------------------------------------------------------------------------
					case RestRequestResult.Timeout            : Content = 
						 string.Format ( Content, "TIMEOUT"             , "block", "none" ); break;
					//-----------------------------------------------------------------------------------------------------------
